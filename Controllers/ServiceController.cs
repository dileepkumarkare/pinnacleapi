    [Route("api/[controller]")]
    [ApiController]
    public class ServiceController : ControllerBase
    {
        ServiceModel model = new ServiceModel();
        MasterModel masterModel = new MasterModel();


        [HttpPost]
        [Route("GetAllServices")]
        public IActionResult GetAllServices(Pagination entity)
        {
            string token = Request.Headers["Authorization"];
            Ret tokenStatus = masterModel.CheckToken(token);
            Ret accessStatus = masterModel.CheckAceess(true);
            Ret res = tokenStatus.IstokenExpired == true ? tokenStatus : accessStatus.status ? model.GetAllServices(entity, tokenStatus.data) : accessStatus;
            return Ok(new
            {
                status = res.status,
                IstokenExpired = tokenStatus.IstokenExpired ?? false,
                message = res.message,
                data = res.data,
                totalCount = res.totalCount ?? 0
            });
        }
